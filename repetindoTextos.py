texto = input("Digite um texto: ")
quantidade = int(input("Digite um número inteiro: "))

print(texto * quantidade)

# Este código solicita ao usuário que insira um texto e um número inteiro,
# e então imprime o texto repetido o número de vezes especificado pelo usuário.

# Exemplo de uso:
# Se o usuário digitar "Dia" e depois "3",
# a saída será: DiaDiaDia